DROP TABLE MODE_PAYE;
CREATE TABLE MODE_PAYE (ID INT NOT NULL GENERATED ALWAYS AS IDENTITY,
                       DES VARCHAR(25) NOT NULL UNIQUE,
                       CONSTRAINT MODE_PAYE PRIMARY KEY (ID));
