CREATE VIEW V_MODE_PAYE (ID, "Mode") AS
SELECT * FROM MODE_PAYE;
--~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~--
